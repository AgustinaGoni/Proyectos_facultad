package modelo;

/**
 *
 * @author agus
 */
public abstract class Efecto {
    private String nombre;

    public Efecto(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    @Override
    public String toString() {
        return nombre;
    }
    
    public abstract boolean esNumeroValido(int n,Mesa mesa);
}
